#დავალება: შექმენით დიაპაზონი, სადაც გამოიტანთ 0-დან 10-მდე, მხოლოდ ლუწ რიცხვს.

for i in range(0, 10 + 1, 2):
     print(i)



#დავალება: მომხმარებელს შემოატანინეთ რიცხვი, და შემდეგ დაპრინტეთ, მომხმარებლის შემოტანილი რიცხვის ჩათვლით ყველა რაიცხვი.

number = int(input("Enter your number: "))

for i in range(number + 1):
    print(i)


